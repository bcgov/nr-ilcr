-- Story 14.1 (Schedule 8 / Tree to Truck) test-schema EXTENSION (never edit V1-V10). Adds the three
-- legacy THE hierarchy tables + the eight code tables the read's label lookups need, their sequences,
-- category-'8' cost items, and a three-level seed. Shapes transcribed from the legacy model classes;
-- code-table shape + the addition/deduction subcategory ids CONFIRMED against the delivery DB
-- (2026-07-22): additions = ILCR_SUBCATEGORY_ID '1'/'2', deductions = '3'/'4' (Constant.java + dev DB);
-- every code table carries a DESCRIPTION column. V11 is the next free migration number.

-- ================================================================================================
-- Hierarchy tables (test-scope; the real THE schema owns the full column set + FK constraints).
-- ================================================================================================
CREATE TABLE THE.TREE_TO_TRUCK_REPORT (
  TREE_TO_TRUCK_REPORT_ID   NUMBER(10) PRIMARY KEY,
  REPORT_YEAR               NUMBER(10),
  ILCR_MILL_ID              NUMBER(10),
  ILCR_CATEGORY_ID          VARCHAR2(3),
  ILCR_SUPPORT_CENTRE_CODE  VARCHAR2(10),
  ILCR_FOREST_REGION_CODE   VARCHAR2(10),
  BEC_ZONE_CODE             VARCHAR2(10),
  TSA_NUMBER                VARCHAR2(10),
  TSB_NUMBER_CODE           VARCHAR2(10),
  TFL_NUMBER_CODE           VARCHAR2(10),
  CUTTING_PERMIT_NUMBER     VARCHAR2(10),
  HARVEST_LICENSE_NUMBER    VARCHAR2(8),
  DIVISION_LOCATION         VARCHAR2(30),
  CONTACT_NAME              VARCHAR2(50),
  CONTACT_PHONE_NUMBER      VARCHAR2(20),
  COMMENTS                  VARCHAR2(2000),
  REVISION_COUNT            NUMBER(10) DEFAULT 0,
  ENTRY_USERID              VARCHAR2(30),
  ENTRY_TIMESTAMP           TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATE_USERID             VARCHAR2(30),
  UPDATE_TIMESTAMP          TIMESTAMP
);

CREATE TABLE THE.TREE_TO_TRUCK_DETAIL_REPORT (
  TREE_TO_TRUCK_DETAIL_REPORT_ID NUMBER(10) PRIMARY KEY,
  TREE_TO_TRUCK_REPORT_ID        NUMBER(10),
  CONTRACTOR_ID                  VARCHAR2(12),
  CUT_BLOCK                      VARCHAR2(12),
  GROUND_BASE_PCT                NUMBER(3),
  GRAPPLE_PCT                    NUMBER(3),
  SKYLINE_PCT                    NUMBER(3),
  HIGHLEAD_PCT                   NUMBER(3),
  HELICOPTER_PCT                 NUMBER(3),
  OTHER_SKIDDING_PCT             NUMBER(3),
  SKYLINE_SLOPE_DISTANCE         NUMBER(10),
  SKYLINE_SUPPORT_NUMBER         NUMBER(10),
  SUPPORT_AVERAGE_DISTANCE       NUMBER(18,4),
  CYCLE_TIME                     NUMBER(18,4),
  DISTANCE                       NUMBER(18,4),
  WATER_DUMP_DESTINATION_IND     VARCHAR2(1),
  UPHILL_DIRECTION_IND           VARCHAR2(1),
  ILCR_SKID_TYPE_CODE            VARCHAR2(10),
  SKID_TYPE_DESCRIPTION          VARCHAR2(50),
  CONIFEROUS_VOLUME              NUMBER(10),
  DECIDUOUS_VOLUME               NUMBER(10),
  ORIGINAL_TREE_TO_TRUCK_RATE    NUMBER(18,4),
  COMMENTS                       VARCHAR2(2000),
  REVISION_COUNT                 NUMBER(10) DEFAULT 0,
  ENTRY_USERID                   VARCHAR2(30),
  ENTRY_TIMESTAMP                TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATE_USERID                  VARCHAR2(30),
  UPDATE_TIMESTAMP               TIMESTAMP
);

CREATE TABLE THE.TREE_TO_TRUCK_RATE_DETAIL (
  TREE_TO_TRUCK_RATE_DETAIL_ID   NUMBER(10) PRIMARY KEY,
  TREE_TO_TRUCK_DETAIL_REPORT_ID NUMBER(10),
  ILCR_RATE_COST_TYPE_CODE       VARCHAR2(10),
  ILCR_REPORT_COST_ITEM_ID       NUMBER(10),
  ITEM_DESCRIPTION               VARCHAR2(30),
  COSTING_RATE                   NUMBER(18,4),
  RATE_COST_TYPE_DESCRIPTION     VARCHAR2(30),
  REVISION_COUNT                 NUMBER(10) DEFAULT 0,
  ENTRY_USERID                   VARCHAR2(30),
  ENTRY_TIMESTAMP                TIMESTAMP DEFAULT SYSTIMESTAMP,
  UPDATE_USERID                  VARCHAR2(30),
  UPDATE_TIMESTAMP               TIMESTAMP
);

CREATE SEQUENCE THE.TREE_TO_TRUCK_REPORT_SEQ START WITH 9000 INCREMENT BY 1;
CREATE SEQUENCE THE.TREE_TO_TRUCK_DTL_REPORT_SEQ START WITH 9000 INCREMENT BY 1;
CREATE SEQUENCE THE.TREE_TO_TRUCK_RATE_DETAIL_SEQ START WITH 9000 INCREMENT BY 1;

-- ================================================================================================
-- Code tables (delivery-DB shape: <CODE>, DESCRIPTION, + effective-dating). The read joins each
-- FK to its DESCRIPTION for label lookups (Story 14.1 §Decision 3).
-- ================================================================================================
CREATE TABLE THE.ILCR_SUPPORT_CENTRE_CODE (ILCR_SUPPORT_CENTRE_CODE VARCHAR2(10) PRIMARY KEY, DESCRIPTION VARCHAR2(120));
CREATE TABLE THE.ILCR_FOREST_REGION_CODE (ILCR_FOREST_REGION_CODE VARCHAR2(10) PRIMARY KEY, DESCRIPTION VARCHAR2(120));
CREATE TABLE THE.BEC_ZONE_CODE (BEC_ZONE_CODE VARCHAR2(10) PRIMARY KEY, DESCRIPTION VARCHAR2(120));
CREATE TABLE THE.TSA_NUMBER_CODE (TSA_NUMBER VARCHAR2(10) PRIMARY KEY, DESCRIPTION VARCHAR2(120));
CREATE TABLE THE.TSB_NUMBER_CODE (TSB_NUMBER_CODE VARCHAR2(10) PRIMARY KEY, DESCRIPTION VARCHAR2(120));
CREATE TABLE THE.TFL_NUMBER_CODE (TFL_NUMBER VARCHAR2(10) PRIMARY KEY, DESCRIPTION VARCHAR2(120));
CREATE TABLE THE.ILCR_SKID_TYPE_CODE (ILCR_SKID_TYPE_CODE VARCHAR2(10) PRIMARY KEY, DESCRIPTION VARCHAR2(120));
CREATE TABLE THE.ILCR_RATE_COST_TYPE_CODE (ILCR_RATE_COST_TYPE_CODE VARCHAR2(10) PRIMARY KEY, DESCRIPTION VARCHAR2(120));

