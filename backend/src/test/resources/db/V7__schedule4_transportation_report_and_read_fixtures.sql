-- V1's test snapshot only carries ILCR_REPORT_SUMMARY_ID, so add the transportation FK column here.
ALTER TABLE THE.ILCR_COST_REPORT_DETAIL ADD (TRANSPORTATION_REPORT_ID NUMBER(10));

